// addition.ts

// Function with type annotations
function addNumbers(a: number, b: number): number {
    let result = a + b;
    return result;
}

// Variables
let num1: number = 10;
let num2: number = 30;

// Call function
let result: number = addNumbers(num1, num2);

// Print output
console.log("The sum is:", result);

